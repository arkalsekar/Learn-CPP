#include <iostream>
#include <cmath>

using namespace std;
int main () {
    cout << "Math and Math Functions" << endl;
    int a =20, b =23;
    cout << max(a, b) << endl;
    cout << min(a, b) << endl;
    cout << sqrt(a) << endl;
    cout << ceil(2.3) << endl;
    cout << floor(2.3) << endl;
    cout << round(2.6) << endl;
    cout << log(2) << endl;
}   
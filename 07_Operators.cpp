#include <iostream>
using namespace std;
int main () {
    int x = 10, y = 10;
    cout << "Operators in CPP" << endl;
    // below are the operators in cpp 
    // 1. Arithematic Operator
    cout << x + y << endl; // Addition
    cout << x - y << endl; // Subtraction
    cout << x * y << endl; // Multiplication
    cout << x / y << endl; // Division
    cout << x % y << endl; // Modulus

    // 2. Complex Arithematic 

    // 3. Comparision Operator 
    cout << (x > y) << endl;
    cout << (x < y) << endl;
    cout << (x == y) << endl;
    cout << (x >= y) << endl;
    cout << (x <= y) << endl;
    cout << (x != y) << endl;

    // 4. Logical Operator
}